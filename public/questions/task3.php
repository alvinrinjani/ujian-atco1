Perhatikan code php berikut:

<?php
    $nama1 = 'Ahmad Mahmud';
    $nama2 = 'Razmi Alfi';
    $nama3 = 'Abdullah Abduh ';
?>

Buatlah pengkondisian (decision making) jika jumlah huruf pada nama tersebut (setelah dikurangi spasi) lebih dari 10 karakter, maka tampilkan nama tersebut, dan jika hurufnya kurang dari 10 karakter maka nama tidak perlu ditampilkan.